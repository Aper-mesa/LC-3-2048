.ORIG x3000
CONDITION1: .FILL x0004
CONDITION2: .FILL x0004
ZERO: .FILL x0000
RESULT: .FILL x0000
LD R0, ZERO
LD R3, ZERO
LD R4, ZERO
LD R2, CONDITION2
LD R1, CONDITION1
OUTER: ADD R1, R1, #0
       BRz DONE
       ADD R1, R1, #-1
       ADD R3, R3, #1
       LD R2, CONDITION2
       BR INNER
INNER: ADD R2, R2, #0
       BRz OUTER
       ADD R2, R2, #-1
       ADD R4, R4, #1
       BR INNER
DONE: ADD R0, R3, R4
      ST R0, RESULT
      HALT
      .END